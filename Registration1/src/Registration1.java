    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String gender = jRadioButton2.isSelected() ? "Male" : "Female";
        String dob = jComboBox1.getSelectedItem() + "-" + jComboBox2.getSelectedItem() + "-" + jComboBox3.getSelectedItem();
        String address = jTextField3.getText();

        if (checkbox1.getState()) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{name, mobile, gender, dob, address});
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please accept the terms and conditions.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

